package problem.day22;

import java.util.Stack;

public class P1roblem55 {
    Stack<Integer> min = new Stack<>();
    Stack<Integer> stack = new Stack<>();
    public void push(int x) {
        stack.push(x);
        if (min.isEmpty() || min.peek() >= x) {
            min.push(x);
        }
    }

    public void pop() {
        if (stack.pop().equals(min.peek())) {
            min.pop();
        }
    }

    public int top() {
        return stack.peek();
    }

    public int getMin() {
        return min.peek();
    }
}
