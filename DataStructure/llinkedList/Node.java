package llinkedList;

public class Node {
    //member
    private int data;
    private Node next;
    
    //method
    public int getData() {
        return data;
    }
    public void setData(int data) {
        this.data = data;
    }
    public Node getNext() {
        return next;
    }
    public void setNext(Node next) {
        this.next = next;
    }

}
